package writing;

public class Writing {
	
	private int writingID;
	private String writingTitle;
	private String userID;
	private String writingDate;
	private String writingContent;
	private int writingaVailable;
	
	public int getWritingID() {
		return writingID;
	}
	public void setWritingID(int writingID) {
		this.writingID = writingID;
	}
	public String getWritingTitle() {
		return writingTitle;
	}
	public void setWritingTitle(String writingTitle) {
		this.writingTitle = writingTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getWritingDate() {
		return writingDate;
	}
	public void setWritingDate(String writingDate) {
		this.writingDate = writingDate;
	}
	public String getWritingContent() {
		return writingContent;
	}
	public void setWritingContent(String writingContent) {
		this.writingContent = writingContent;
	}
	public int getWritingaVailable() {
		return writingaVailable;
	}
	public void setWritingaVailable(int writingaVailable) {
		this.writingaVailable = writingaVailable;
	}

}
